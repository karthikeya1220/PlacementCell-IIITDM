// Calculate statistics from the data
export const calculateStatistics = () => {
  const totalOffers = placementData2024_25.length
  const uniqueStudents = 149 // As mentioned in the data

  // Calculate gender distribution
  const genderDistribution = {
    male: placementData2024_25.filter((entry) => entry.gender === "M").length,
    female: placementData2024_25.filter((entry) => entry.gender === "F").length,
  }

  // Calculate department-wise distribution
  const departmentCounts: Record<string, number> = {}
  placementData2024_25.forEach((entry) => {
    if (!departmentCounts[entry.department]) {
      departmentCounts[entry.department] = 0
    }
    departmentCounts[entry.department]++
  })

  // Calculate program-wise distribution
  const programCounts: Record<string, number> = {}
  placementData2024_25.forEach((entry) => {
    if (!programCounts[entry.program]) {
      programCounts[entry.program] = 0
    }
    programCounts[entry.program]++
  })

  // Calculate offer type distribution
  const offerTypeCounts: Record<string, number> = {}
  placementData2024_25.forEach((entry) => {
    if (!offerTypeCounts[entry.offerType]) {
      offerTypeCounts[entry.offerType] = 0
    }
    offerTypeCounts[entry.offerType]++
  })

  // Calculate salary statistics
  const salaries = placementData2024_25
    .filter((entry) => entry.salary && typeof entry.salary === "number")
    .map((entry) => entry.salary as number)

  const avgSalary = salaries.length > 0 ? salaries.reduce((sum, salary) => sum + salary, 0) / salaries.length : 0

  const maxSalary = salaries.length > 0 ? Math.max(...salaries) : 0

  // Get top recruiting companies
  const companyCounts: Record<string, number> = {}
  placementData2024_25.forEach((entry) => {
    if (!companyCounts[entry.company]) {
      companyCounts[entry.company] = 0
    }
    companyCounts[entry.company]++
  })

  const topCompanies = Object.entries(companyCounts)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 10)
    .map(([company, count]) => ({ company, count }))

  // Calculate salary range distribution
  const salaryRanges = {
    "< 5 LPA": 0,
    "5-7 LPA": 0,
    "7-10 LPA": 0,
    "10-15 LPA": 0,
    "> 15 LPA": 0,
  }

  salaries.forEach((salary) => {
    const salaryInLakhs = salary / 100000
    if (salaryInLakhs < 5) salaryRanges["< 5 LPA"]++
    else if (salaryInLakhs < 7) salaryRanges["5-7 LPA"]++
    else if (salaryInLakhs < 10) salaryRanges["7-10 LPA"]++
    else if (salaryInLakhs < 15) salaryRanges["10-15 LPA"]++
    else salaryRanges["> 15 LPA"]++
  })

  return {
    totalOffers,
    uniqueStudents,
    genderDistribution,
    departmentCounts,
    programCounts,
    offerTypeCounts,
    avgSalary,
    maxSalary,
    topCompanies,
    salaryRanges,
  }
}

export const statistics = calculateStatistics()

// Prepare data for charts
export const prepareChartData = () => {
  const stats = calculateStatistics()

  // Department-wise data for charts
  const departmentData = Object.entries(stats.departmentCounts)
    .filter(([dept]) => dept !== "M.Des") // Filter out departments with very few entries
    .map(([department, count]) => {
      // Calculate average CTC for each department
      const deptEntries = placementData2024_25.filter(
        (entry) => entry.department.includes(department) && typeof entry.salary === "number",
      )
      const avgCTC =
        deptEntries.length > 0
          ? deptEntries.reduce((sum, entry) => sum + (entry.salary as number), 0) / deptEntries.length / 100000
          : 0

      return {
        department,
        offers: count,
        avgCTC: Number.parseFloat(avgCTC.toFixed(1)),
      }
    })
    .sort((a, b) => b.offers - a.offers)

  // Program-wise data for charts
  const programData = Object.entries(stats.programCounts)
    .map(([name, value]) => {
      // Calculate average CTC for each program
      const progEntries = placementData2024_25.filter(
        (entry) => entry.program === name && typeof entry.salary === "number",
      )
      const ctc =
        progEntries.length > 0
          ? progEntries.reduce((sum, entry) => sum + (entry.salary as number), 0) / progEntries.length / 100000
          : 0

      return {
        name,
        value,
        color: name === "B.Tech" ? "#4DA8DA" : name === "M.Tech" ? "#0A2463" : "#10B981",
        ctc: Number.parseFloat(ctc.toFixed(1)),
      }
    })
    .sort((a, b) => b.value - a.value)

  // Offer type data for charts
  const offerTypeData = Object.entries(stats.offerTypeCounts).map(([name, value]) => ({
    name,
    value,
    color: name === "Full Time" ? "#4DA8DA" : name === "Intern cum PPO" ? "#0A2463" : "#10B981",
  }))

  // Gender distribution data for charts
  const genderData = Object.entries(stats.genderDistribution).map(([gender, count]) => ({
    name: gender === "male" ? "Male" : "Female",
    value: count,
    color: gender === "male" ? "#4DA8DA" : "#EC4899",
  }))

  // Salary range data for charts
  const salaryRangeData = Object.entries(stats.salaryRanges).map(([range, count]) => ({
    range,
    count,
  }))

  // Top companies data
  const topCompaniesData = stats.topCompanies.map((company) => ({
    ...company,
    avgCTC: 0, //parseFloat(company.avgCTC.toFixed(2)),
  }))

  return {
    departmentData,
    programData,
    offerTypeData,
    genderData,
    salaryRangeData,
    topCompaniesData,
  }
}
